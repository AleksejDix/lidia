function generateRandomPeople(count) {
  const people: any[] = []
  for (let i = 0; i < count; i++) {
    const person = {
      name: countries[i],
      id: countries[i]
    }
    people.push(person)
  }
  return people
}

export const people = generateRandomPeople(countries.length)

export async function searchSwissCantons(query = '', selectedCantons: number[] = []) {
  if (!query) {
    return []
  }

  const searchQuery = query.toLowerCase()
  return swissCantons.filter(
    (canton) =>
      canton.title.toLowerCase().startsWith(searchQuery) && !selectedCantons.includes(canton.id)
  )
}
